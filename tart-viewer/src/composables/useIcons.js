// Create icon component function
function createIcon(iconName, { size = 24, color = 'currentColor' } = {}) {
  const iconData = svgIcons[iconName]
  
  if (!iconData) {
    console.warn(`Icon "${iconName}" not found. Available icons:`, Object.keys(svgIcons))
    return null
  }

  return h('svg', {
    width: size,
    height: size,
    viewBox: iconData.viewBox,
    fill: color,
    style: {
      display: 'inline-block',
      verticalAlign: 'middle'
    }
  }, [
    h('path', { d: iconData.path })
  ])
}

// Icon component for use in templates
export const SvgIcon = {
  name: 'SvgIcon',
  props: {
    icon: {
      type: String,
      required: true
    },
    size: {
      type: [Number, String],
      default: 24
    },
    color: {
      type: String,
      default: 'currentColor'
    }
  },
  render() {
    return createIcon(this.icon, {
      size: this.size,
      color: this.color
    })
  }
}

// Composable for programmatic icon usage
export function useIcons() {
  return {
    createIcon,
    availableIcons: Object.keys(svgIcons),
    hasIcon: (iconName) => iconName in svgIcons
  }
}

